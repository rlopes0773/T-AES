const size_t aes_test_vector_count = sizeof(aes_test_vectors) / sizeof(aes_test_vectors[0]);

// Simple loader helper: returns pointer to vector array and count via out parameters.
void load_aes_test_vectors(const aes_tv_t** out_vecs, size_t* out_count) {
    if (out_vecs) *out_vecs = aes_test_vectors;
    if (out_count) *out_count = aes_test_vector_count;
}
